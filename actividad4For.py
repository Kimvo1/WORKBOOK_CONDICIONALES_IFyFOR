
#Crear un programa que reciba una lista de números y calcule la suma de los mismos utilizando un ciclo for
lista_numeros = [2, 3, 4, 8, 9 ]
suma = 0
for numero in lista_numeros:
    suma += numero
    print(suma)
# Crear un programa que reciba una cadena de caracteres y muestre cada letra en una línea distinta utilizando un ciclo for
cadena = input("escriba una palabra: ")
for letra in cadena:
        print(letra)
    